<h1 class="font-serif text-4xl mb-4">Resourcing Open Source</h1>
<article class="leading-relaxed text-lg">
  <p class="mb-3">
    Open source software projects have become akin to public utilities, utilized by more than 90% of
    software applications<sup><a href="#1">1</a></sup>. But these projects are often fragile,
    passing along security vulnerabilities, suffering “bit rot,” and abandonment by maintainers.
  </p>
  <p class="mb-3">
    We believe that the root cause is a lack of project <i>sustainability</i>&mdash;stakeholders
    lack the necessary resources (time, money, and skill) to adequately steward the project for the
    long-term.
  </p>

  <p>
    <i class="text-neutral-500">
      Read more about our analysis of the problem <a
        class="border-b-2 hover:border-b-2 hover:border-yellow-700 font-semibold text-neutral-500 hover:text-yellow-700"
        href="/open-source/problem">here</a
      >.
    </i>
  </p>

  <h2 class="text-2xl mt-5 mb-3">A Way Forward: Maximize the Impact of Developer Hours</h2>
  <p class="mb-3">
    We believe that the <a
      class="border-b-2 hover:border-b-2 hover:border-yellow-700 hover:text-yellow-700"
      href="https://en.wikipedia.org/wiki/Rivalry_(economics)">rivalrous good</a
    > in the creation and maintenance of open source software is not the software, i.e. the bits, but
    rather the production process itself. That leads to a tangible goal:
  </p>
  <p class="mb-8 mt-5 text-xl text-center ml-20 mr-20 italic  text-neutral-700">
    Pioneer new economic systems that maximize the impact of developer hours in the production of
    open source software.
  </p>
  <p class="mb-3">
    At a high level, we are interested in solutions that explore one of four strategies:
  </p>
  <ol class="list-decimal mb-5">
    <li class="ml-8">
      <p>Increase the total number of available hours.</p>
    </li>
    <li class="ml-8">
      <p>Increase the efficiency of existing hours.</p>
    </li>
    <li class="ml-8">
      <p>Eliminate wasteful hours.</p>
    </li>
    <li class="ml-8">
      <p>Redirect hours to higher priority tasks and projects.</p>
    </li>
  </ol>
</article>
